// Add takes two integers and returns their sum
// This demonstrates:
// - Basic function syntax in Go
// - Multiple parameters of the same type (x, y int)
// - Single return value
//
// Parameters:
//   x - first integer to add
//   y - second integer to add
//
// Returns:
//   The sum of x and y
func Add(x, y int) int {
	return x + y
}
